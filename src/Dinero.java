
public abstract class Dinero {

	
	protected double dinero;
	protected String description;
	
	
	
	
	double getDinero(){
		
		return dinero;
		
	}
	
	void setDinero(double dinero){
		
		this.dinero=dinero;
		
	}
	
	
	String getDescription() {
		
		return description;
	}
	
	void setDescription(String description) {
		
		this.description=description;
	}
	
}
